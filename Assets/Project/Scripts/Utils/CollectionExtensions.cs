using System;
using System.Collections.Generic;
using UnityEngine;

namespace Utils
{
    public static class CollectionExtensions
    {
        // Перемешивает список на месте (самый эффективный вариант)
        public static void Shuffle<T>(this IList<T> list)
        {
            System.Random rng = new System.Random();
            int n = list.Count;
            while (n > 1)
            {
                n--;
                int k = rng.Next(n + 1);
                T value = list[k];
                list[k] = list[n];
                list[n] = value;
            }
        }

        // Вариант с использованием Unity Random
        public static void ShuffleUnity<T>(this IList<T> list)
        {
            int n = list.Count;
            while (n > 1)
            {
                n--;
                int k = UnityEngine.Random.Range(0, n + 1);
                T value = list[k];
                list[k] = list[n];
                list[n] = value;
            }
        }
    }
}